year_Pushkin = int(input('Какой год рождения А.С Пушкина?'))

while year_Pushkin != 1799:
    print('Неверный год')
    year_Pushkin = int(input('Какой год рождения А.С Пушкина?'))

day_of_born_Pushkin = int(input('Какой день рождения А.С Пушкина?'))
while day_of_born_Pushkin != 6:
    print('Неверный день')
    day_of_born_Pushkin = int(input('Какой день рождения А.С Пушкина?'))

print('Верно')